# Write a single Python statement that combines the three strings "My 
# name is", "Joe" and "Warren" (plus a couple of other small strings) 
# into one larger string "My name is Joe Warren." and prints the result.

first_string = "My name is"
first_name = "Joe"
last_name = "Warren"
space = " "
period = "."

string = first_string + space + first_name + space + last_name + period
print(string)