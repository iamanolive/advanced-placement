# The area of a circle is πr^2 where r is the radius of the circle. 
# (The raised 2 in the formula is an exponent.) Write a Python 
# statement that calculates and prints the area in square inches of 
# a circle whose radius is 8 inches. Assume that the constant π = 3.14.

radius = 8
PI = 3.14
print(PI * (radius ** 2))